<div class="container pt-5">
	<h2 class="text-center azul">¿Qué prima de decesos es mejor contratar?</h2><hr>
	<p class="pb-3">No hay un tipo de prima mejor que otro a la hora de contratar un seguro de decesos. Si bien es cierto que es recomendable tener en cuenta cómo va a evolucionar nuestra póliza en cuanto al tema económico se refiere con el paso de los años. Pero según comentamos anteriormente no hay primas mejores o peores sino que ofrecen un desarrollo distinto.</p>
	<p class="text-left azul titulo-descripcion"><strong>Existen 5 tipos de primas a la hora de contratar un seguro de decesos</strong></p>
  <p class="pb-4">
     Los diferentes tipos de seguros de decesos varían en función de la edad y la forma de pago de la prima. Pero todos tienen como objetivo cubrir el servicio funerario en caso de fallecimiento del asegurado.
  </p>
	<div class="row align-items-center border p-4 tipo_prima">
		<div class="col-12">
			<h4 class="naranja text-center">Prima Natural</h4>
		</div>
		<div class="col-md-5 col-lg-4"><img alt="Prima Natural" class="mx-auto d-block pb-4 pt-3" src="/images/prima-natural.svg" title="Seguro de Decesos Prima Natural" width="260" height="260"></div>
		<div class="col-md-7 col-lg-8">
			<p>En la prima natural <strong>el aumento de la prima va relacionado con la edad, a mayor edad más prima</strong>. Esta póliza tiene una renovación anual.</p>
			<p>La mayor subida suele ser a partir de los 70 años en compensación del precio más económico en menor edad.</p>
		</div>
	</div>
	<div class="row align-items-center border p-4 tipo_prima">
		<div class="col-12">
			<h4 class="naranja text-center">Prima Mixta</h4>
		</div>
		<div class="col-md-5 col-lg-4"><img alt="Prima Mixta" class="mx-auto d-block pb-4 pt-3" src="/images/prima-mixta.svg" title="Seguro de Decesos Prima Mixta" width="260" height="260"></div>
		<div class="col-md-7 col-lg-8">
			<p>La prima mixta es una variable de la prima anterior, <strong>a edades tempranas sale más económico</strong> y dado que no hay derecho de antigüedad <strong>a partir de los 65/70 años</strong> aproximadamente en función de la compañía aumenta el precio a partir de esa edad <strong>pasa a ser prima nivelada</strong>. Las actualizaciones de la prima suelen ser cada año.</p>
		</div>
	</div>
  <div class="row align-items-center border p-4 tipo_prima">
		<div class="col-12">
			<h4 class="naranja text-center">Prima Seminatural</h4>
		</div>
		<div class="col-md-5 col-lg-4"><img alt="Prima Mixta" class="mx-auto d-block pb-4 pt-3" src="/images/prima-seminatural.svg" title="Seguro de Decesos Prima Seminatural" width="260" height="260"></div>
		<div class="col-md-7 col-lg-8">
           <p>La prima seminatural funciona igual que la mixta, con la variante de que <strong>las renovaciones por edad se producen en tramos de 5 años</strong> y no todas las primas seminaturales acaban pasando a ser niveladas.
          </p>
		</div>
	</div>
	<div class="row align-items-center border p-4 tipo_prima">
		<div class="col-12">
			<h4 class="naranja text-center">Prima Nivelada</h4>
		</div>
		<div class="col-md-5 col-lg-4"><img alt="Prima Nivelada" class="mx-auto d-block pb-4 pt-3" src="/images/prima-nivelada.svg" title="Seguro de Decesos Prima Nivelada" width="260" height="260"></div>
		<div class="col-md-7 col-lg-8">
			<p>La prima nivelada como su nombre indica <strong>mantiene constante la prima del asegurado durante el seguro</strong> a excepción de aumento de capital. De esta manera, desde el momento de contratación el asegurado sabe lo que va a pagar, y las subidas son más reducidas.</p>
			<p>Es una prima más cara al principio y a partir de la edad de jubilación la prima sale más económica en comparación con las anteriores.</p>
		</div>
	</div>
	<div class="row align-items-center border p-4 tipo_prima">
		<div class="col-12">
			<h4 class="naranja text-center">Prima Única</h4>
		</div>
		<div class="col-md-5 col-lg-4"><img alt="Prima Única" class="mx-auto d-block pb-4 pt-3" src="/images/prima-unica.svg" title="Seguro de Decesos Prima Única" width="260" height="260"></div>
		<div class="col-md-7 col-lg-8">
			<p>Esta modalidad es <strong>para personas mayores de 65 o 70 años</strong> (depende de la compañía) que no han suscrito un seguro de decesos de los mencionados anteriormente. La contratación <strong>se realiza mediante un único pago</strong>.</p>
			<p>Con una sóla aportación de capital da cobertura en caso de fallecimiento del asegurado.</p>
		</div>
	</div>
	<p class="py-5">Dependiendo de las edades a la hora de la contratación y como queramos encaminar la evolución de la póliza a contratar, podríamos decir que unas primas son más aconsejadas que otras. Pero en ningún caso se han de valorar como mejores o peores dado que cada tipo prima corresponde a unas necesidades y preferencias de cada persona o grupo familiar.</p>
</div>